// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.
12 % 5 // retorna 2.

// Incremento (++) e Decremento (--)
const a = ++2; // Recebe 3 direto.
const b = 2++; // Recebe 2 e depois vira 3.

// Negação (-) e Adição (+)
-3          // nega 3
+"3"        // retorna 3
+true       // retorna 1
+false      // retorna 0
-true       // retorna -1

// Operador de exponenciação (**)
2 ** 3      // retorna 8
10 ** -1    // retorna 0.1

// Operador de agrupamento ( )
2 * (3 + 2)




